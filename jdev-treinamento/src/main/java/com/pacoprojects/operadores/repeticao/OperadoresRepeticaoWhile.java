package com.pacoprojects.operadores.repeticao;

public class OperadoresRepeticaoWhile {

	public static void main(String[] args) {

//		Estrutura de Repeticao WHILE
		int numero = 0;
		
		while (numero <= 10) { // Primeiro Verifica depois executa
			System.out.println("O numero atual eh: " + numero);
			numero++;

		}
	}
}
