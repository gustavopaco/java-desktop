package com.pacoprojects.operadores.aritmeticos;

public class OperadoresAritimeticos {

	public static void main(String[] args) {

		double n1 = 50;
		double n2 = 78;

		System.out.println("Adicao = " + (n1 + n2)); // Adicao = 128.0
		System.out.println("Subtracao = " + (n1 - n2)); // Subtracao = -28.0
		System.out.println("Multiplicacao = " + (n1 * n2)); // Multiplicacao = 3900.0
		System.out.println("Divisao = " + (n1 / n2)); // Divisao = 0.6410256410256411
		System.out.println("Modulo = " + (n1 % n2)); // Modulo = 50.0 | Modulo é o resto da divisão.

	}
}
