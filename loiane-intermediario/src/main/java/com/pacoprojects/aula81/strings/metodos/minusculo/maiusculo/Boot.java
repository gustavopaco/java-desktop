package main.java.com.pacoprojects.aula81.strings.metodos.minusculo.maiusculo;

public class Boot {
    public static void main(String[] args) {
        String teste = "Teste";

        // Note: toLowerCase() -> Cria um nova String com todos os caracteres em minusculo.
        System.out.println(teste.toLowerCase());
        // Note: toUpperCase() -> Cria um nova String com todos os caracteres em maiusculo.
        System.out.println(teste.toUpperCase());
    }
}
