package com.pacoprojects.aula05.tipos.primitivos;

public class VariaveisFlutuantes {
    public static void main(String[] args) {
        float valorPassagem = 4.20f;
        double valorTomate = 6.20;

        System.out.println("Valor variável valor da passagem = " + valorPassagem);
        System.out.printf("Valor variável valor da passagem = %.2f\n", valorPassagem);
        System.out.println("Valor variável valor do tomate = " + valorTomate);
    }
}
